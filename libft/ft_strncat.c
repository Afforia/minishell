#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*dest;
	size_t	i;

	dest = s1;
	i = 0;
	while (*dest != '\0')
		dest++;
	while (*s2 != '\0' && i < n)
	{
		*dest++ = *s2++;
		i++;
	}
	*dest = '\0';
	return (s1);
}
